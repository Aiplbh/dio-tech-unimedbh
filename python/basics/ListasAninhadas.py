# Aplicação da aula sobre Listas aninhadas


# Listas podem armazenar outras listas premitindo a criação de estruturas matriciais

matriz = [[1,2,3],[4,5,6],[7,8,9]]

# matriz[0] -------> [1,2,3]
# matriz[1][-1] ---> [5]
# martriz[0][1] ---> [2]

print(matriz[0])     # ----> [1,2,3]
print(matriz[1][-1]) # ----> [6]
print(matriz[0][1]) #  ----> [2]

